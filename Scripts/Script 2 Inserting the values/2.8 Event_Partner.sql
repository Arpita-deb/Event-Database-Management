-- Removing duplicate entries for partners with more than one role for one particular event
-- There were a few partners having same role such as 'Catering Partner' for one event. So, I removed them by selecting only one value.


WITH duplicates AS
(
SELECT event_ID, 
	partner_ID, 
	role,
	rank = ROW_NUMBER() OVER(PARTITION BY event_ID, partner_ID ORDER BY role DESC), -- ranks the record for each event_ID and Partner_ID  
	row_counts = COUNT(*) over (PARTITION BY event_ID, partner_ID)
from Event_Partner
) 
DELETE FROM duplicates
WHERE rank = 2

-- Removing redundant records with more than 1 same type of partners
-- There were a few records where there were partners with similar role for the same event (like partners 345, 609 and 673 all having a role of Catering Partner).

WITH role_duplicates AS
(
SELECT event_ID, 
	partner_ID, 
	role,
	rank = ROW_NUMBER() OVER(PARTITION BY event_ID, role ORDER BY role DESC), -- ranks the record for each event_ID and role  
	row_counts = COUNT(*) over (PARTITION BY event_ID, role)
from Event_Partner
) 
DELETE FROM role_duplicates
WHERE rank >= 2;

